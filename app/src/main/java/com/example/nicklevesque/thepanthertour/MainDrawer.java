/**
 * MainDrawer activity that holds all of my fragments and
 * transitions to them accordingly via side bar or home page
 */

public class MainDrawer extends AppCompatActivity
        implements NavigationView.OnNavigationItemSelectedListener{


    /* Called to do initial creation of the fragment. */
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main_drawer);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        TextView mTitle = (TextView) toolbar.findViewById(R.id.toolbar_title);
        setSupportActionBar(toolbar);
        mTitle.setText(toolbar.getTitle());

        getSupportActionBar().setDisplayShowTitleEnabled(false);


        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        ActionBarDrawerToggle toggle = new ActionBarDrawerToggle(
                this, drawer, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawer.setDrawerListener(toggle);
        toggle.syncState();

        NavigationView navigationView = (NavigationView) findViewById(R.id.nav_view);
        navigationView.setNavigationItemSelectedListener(this);

        FragmentManager fm = getFragmentManager();
        fm.beginTransaction().replace(R.id.main_drawer, new MainFragment()).commit();

    }


    /*
    Method that ensures the user go back to the current fragment open if they press back when the nav bar is open
     */
    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            if (getFragmentManager().getBackStackEntryCount() > 0) {
                getFragmentManager().popBackStack();
            } else {
                super.onBackPressed();
            }
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        menu.clear();
        getMenuInflater().inflate(R.menu.main_drawer, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }


    /*
    Method that handles fragment transactions when they are pressed within the navigation bar
     */
    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        FragmentManager fm = getFragmentManager();
        android.support.v4.app.FragmentManager sfm = getSupportFragmentManager();
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.spots) {
            fm.beginTransaction().replace(R.id.main_drawer, new ListFragment()).addToBackStack("my_fragment").commit();
        }
        //Launches Weather Fragment if Weather is selected from the nav bar
        else if (id == R.id.nav_gallery) {
            fm.beginTransaction().replace(R.id.main_drawer, new MapsFragment()).addToBackStack("my_fragment").commit();

        }
        //Launches SlideShow Fragment if selected form nav bar
        else if (id == R.id.nav_slideshow) {
            fm.beginTransaction().replace(R.id.main_drawer, new SlideShowFragment()).addToBackStack("my_fragment").commit();
        }
        //Launches Weather Fragment if Weather is selected from the nav bar
        else if (id == R.id.weather) {
            fm.beginTransaction().replace(R.id.main_drawer, new WeatherFragment()).addToBackStack("my_fragment").commit();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }




}
